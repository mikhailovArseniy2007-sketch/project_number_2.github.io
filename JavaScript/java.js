// нашёл элемент, в который потом помещу карточку, нашёл элемент где будлут хранится карточки и  записал его в переменную cardSection
const cardSection = document.querySelector('.cardSection');
const button = document.querySelector('.buttonAddCards');
let currentProducts = products;
let showElement = 0;
const step = 8;

// сделай действие, тоесть сейчас и буду тебе говорить что делать с переменной product, а product это просто обозначаем что здесь должна быть переменная
function ProductsAddCard() {
    const nextElement = currentProducts.slice(showElement, showElement + step);
    nextElement.forEach(product => {
        // создал <div>, тоесть карточку
        const card = document.createElement('div');
        card.classList.add('card');
        // создадим переменную которая создаст изображение
        const img = document.createElement('img');
        img.classList.add('cardImg') // дали класс
        img.src = product.img;// присвоили ей img из массива
        // добавим текст
        const title = document.createElement('p');
        title.textContent = product.title;
        title.classList.add('title')
        // добавим цену
        const price = document.createElement('p');
        price.textContent = product.price;
        price.classList.add('price')
        // добавим всё это в карточку
        card.append(img, title, price);
        cardSection.append(card);
    })
    
    showElement += step;
    // Внутри функции ProductsAddCard, в самом конце:

    if (showElement >= currentProducts.length) {
        button.style.display = 'none'; // Скрываем, если товаров больше нет
    } else {
        button.style.display = 'block'; // Показываем, если есть еще что подгружать
    }

}

ProductsAddCard()

button.addEventListener('click', ProductsAddCard)

// 1. Выбираем все кнопки фильтрации
const filterButtons = document.querySelectorAll('.section_button .button');

// 2. Добавляем обработчик события на каждую из них
filterButtons.forEach(btn => {
    btn.addEventListener('click', () => {
        // Получаем текст нажатой кнопки (например, "Для мальчиков")
        const categoryName = btn.textContent.trim();

        // Полностью очищаем секцию с карточками перед новой отрисовкой
        cardSection.innerHTML = '';
        // Сбрасываем счетчик пагинации, чтобы начать с начала списка
        showElement = 0;

        // 3. Создаем отфильтрованный массив
        // Сравниваем категорию товара с текстом на кнопке
        currentProducts = products.filter(product => {
            return product.category === categoryName;
        });

        // 4. Отрисовываем первую порцию карточек новой категории
        ProductsAddCard();
    });
});

