// Функция, которая сработает сразу после загрузки страницы
// Ждем, пока вся страница и другие скрипты загрузятся
window.addEventListener('load', () => {
    const urlParams = new URLSearchParams(window.location.search);
    const rawCategory = urlParams.get('cat');

    if (rawCategory) {
        // 1. Очищаем текст из ссылки от лишних пробелов и переносов
        const categoryFromUrl = decodeURIComponent(rawCategory).replace(/\s+/g, ' ').trim();

        // 2. Сбрасываем текущее состояние
        cardSection.innerHTML = '';
        showElement = 0;

        // 3. Фильтруем массив
        currentProducts = products.filter(product => {
            // Очищаем категорию в массиве для точного сравнения
            const productCat = product.category.trim();
            return productCat === categoryFromUrl;
        });

        // 4. Отрисовываем
        if (currentProducts.length > 0) {
            ProductsAddCard();
        } else {
            console.log("Товары не найдены для категории:", categoryFromUrl);
            // Если ничего не нашли, можно вызвать отрисовку всех товаров
            currentProducts = products;
            ProductsAddCard();
        }
    }
});




